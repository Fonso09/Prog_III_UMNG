package parqueadero;


public class moto {
    String placa;
    String horain;

    public moto(String horain, String placa) {
        this.placa = placa;
        this.horain = horain;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getHorain() {
        return horain;
    }

    public void setHorain(String horain) {
        this.horain = horain;
    }
    
           
}
