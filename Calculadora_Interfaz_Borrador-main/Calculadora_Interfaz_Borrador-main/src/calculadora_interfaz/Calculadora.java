    public static String calculadora(String memoria1, String memoria2, String signo ){
   
        Double resultado=0.0;
        String respuesta;
        switch(signo){
            case "+":
                resultado= Double.parseDouble(memoria1)+Double.parseDouble(memoria2);
                break;
            case "-":
                resultado= Double.parseDouble(memoria1)-Double.parseDouble(memoria2);
                break;
            case"*":
                resultado= Double.parseDouble(memoria1)*Double.parseDouble(memoria2);
                break;
            case"/":
                if(Double.parseDouble(memoria2)!=0){
                resultado= Double.parseDouble(memoria1)/Double.parseDouble(memoria2);
                } else {
                    resultado=101010101010101010101010101010101010101010101010101010101.00;
                }
                break;
            case"%":
                resultado= Double.parseDouble(memoria1)*(Double.parseDouble(memoria2)/100);
                break;
            case"^":
                resultado=Math.pow(Double.parseDouble(memoria1), Double.parseDouble(memoria2));
                break;
            case"√":
                
                if(Double.parseDouble(memoria2)>=0 && Double.parseDouble(memoria1)%2==0 ){
                resultado=Math.pow(Double.parseDouble(memoria2), (1/Double.parseDouble(memoria1)));
                } else {
                    resultado=101010101010101010101010101010101010101010101010101010101.00;
                }
                
                break;
            default:
                resultado=101010101010101010101010101010101010101010101010101010101.00;
                break;
                
        }
        respuesta=resultado.toString();
        return respuesta;
    }
